        <p>Tariffs are government-imposed taxes on imports or exports between countries. When these taxes rise or fall, they directly affect:</p>
        <ul>
          <li>Product pricing</li>
          <li>Consumer demand</li>
          <li>Shipping times</li>
          <li>Search behavior</li>
        </ul>
        <div class="callout strategy">
            <i class="fas fa-lightbulb"></i>
            <p><strong>For example:</strong> If the U.S. imposes new tariffs on tech imports from China, searches for “cheap smartwatches” may spike in India or Mexico instead — shifting global traffic away from your original target market.</p>
        </div>
