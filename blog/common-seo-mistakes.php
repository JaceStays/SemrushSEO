        <h2>‚ö° 3. Slow Website Speed <i class="fas fa-tachometer-alt icon-inline"></i></h2>
        <p>Page loading speed is a crucial ranking factor and directly impacts user experience (UX). A slow website leads to higher bounce rates, lower engagement, and can negatively affect your search rankings, especially with the introduction of Core Web Vitals.</p>
        <div class="callout problem">
            <i class="fas fa-times-circle"></i>
            <p><strong>The Mistake:</strong> A slow-loading website not only frustrates users but also signals a poor user experience to Google, leading to higher bounce rates and lower rankings.</p>
        </div>
        <div class="callout fix">
            <i class="fas fa-tools"></i>
            <p><strong>The Fix:</strong> Use <a href="https://www.semrush.com/features/site-audit/" target="_blank" rel="noopener noreferrer">Semrush Site Audit</a> or Google PageSpeed Insights to identify slow-loading pages and specific performance bottlenecks. Optimize images (compress and use next-gen formats like WebP), enable browser caching, minify CSS/JS files, and consider using a Content Delivery Network (CDN) to serve content faster globally.</p>
        </div>
        <div class="callout pro-tip">
            <i class="fas fa-lightbulb"></i>
            <p><strong>Pro Tip:</strong> Aim for a page speed under 3 seconds on both desktop and mobile for the best user experience and SEO performance, aligning with Google's Core Web Vitals thresholds (Largest Contentful Paint, First Input Delay, Cumulative Layout Shift).</p>
        </div>
